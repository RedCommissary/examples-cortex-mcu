#include "main.h"

/********************************************************************************
 * Variable
 ********************************************************************************/

uint16_t result = 0;

/********************************************************************************
 * Main program body
 ********************************************************************************/

int main (void) {
/***********************************************
 * Setting periphery
 ***********************************************/
    Clock::Init();
    Adc::Init();
    Hrpwm::Init();

    Hrpwm::SetDuty(9000);
    
/***********************************************
 * Setting GPIO for LED indicator on board
 * 
 * PA12 - Set high level (log.1)
 ***********************************************/
    Gpio::Init<12>(GPIOA, Gpio::Mode::output);
    Gpio::Set<12>(GPIOA);

    while(1) {
        result = Adc::Result::inputSignalPA0;
    }
}