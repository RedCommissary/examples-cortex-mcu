/********************************************************************************
 * ADC
 * 
 * Pin voltage input    - PA0   - ADC1 IN1
 * Pin current input    - PA1   - ADC1 IN2
 * Pin current output   - PA2   - ADC1 IN3
 * Pin voltage output   - PA3   - ADC1 IN4
 * Pin temperature      - PA5   - ADC2 IN2
 * 
 ********************************************************************************/

/********************************************************************************
 * Include 
 ********************************************************************************/

#include "main.h"

/********************************************************************************
 * Used function
 ********************************************************************************/

void InitLed() {
    Gpio::Init<15>(GPIOA, Gpio::Mode::output);
    Gpio::Set<15>(GPIOA);
}

/********************************************************************************
 * Main program body
 ********************************************************************************/

int main (void) {

    Clock::Init();
    InitLed();

    while(1) {}
}